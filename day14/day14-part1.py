platform = [[*s] for s in inputs]

for i in range(1, len(platform)):
  rocks = platform[i]

  
  for j, rock in enumerate(rocks):
    base = rock

    k = i - 1
    
    # 内循环：将 base 插入到已排序区间 [0, i-1] 中的正确位置
    while k >= 0 and platform[k][j] == '.' and base == 'O':
        platform[k + 1][j] = platform[k][j]  # 将 nums[j] 向右移动一位
        k -= 1

    platform[k + 1][j] = base  # 将 base 赋值到正确位置

ans = 0
for i, rocks in enumerate(platform):
  count = 0
  for j, rock in enumerate(rocks):
    if rock == 'O':
      count += 1
    print(rock, end='')

  ans += (len(platform) - i) * count
  print()

print(ans)

